package hr.fer.zemris.ecf.log.genotypes;

/**
 * This class is container for all parameters that are in one genotype.
 * This is joust used to help to create {@link AbstractGenotype}. 
 * @version 1.0
 *
 */
public  class InitialGenotype {
	
	/**
	 * Size of a genotype.
	 */
	public int size;
	/**
	 * All value of a genotype represented by a string.
	 */
	public String value;
	/**
	 * Name of the type of the genotype.
	 */
	public String name;

}
